///
/// Simple tool that converts a 1BPP array of data into the block structure used by the
/// display in the Maschine Mikro.
///
/// The display is broken up into 8 pixel bands where each byte represents a vertical
/// slice of the in the band, for a display that is 128pixels wide 128 bytes will
/// represent a slice 128x8 pixels
///
///      0 1 2 3 .. 127
///      --------------
///   0 |
///   1 |
/// B 2 |
/// i 3 |
/// t 4 |
///   5 |
///   6 |
///   7 |
///
/// Update the BITMAP_BUFFER at the end of the file with your output, it is generated
/// from a monochrome bitmap exported from GIMP using C-Header format.
///

fn main() {
   let mut output_buffer = vec![0u8; BITMAP_BUFFER_SIZE / 8];

   // Translate input buffer
   for input_idx in 0..BITMAP_BUFFER_SIZE {
      let byte = BITMAP_BUFFER[input_idx];
      if byte == 1 { continue }  // No work required skip

      let x = input_idx % BITMAP_WIDTH;
      let y = input_idx / BITMAP_WIDTH;

      let output_index = (BITMAP_WIDTH * (y >> 3)) + x;
      output_buffer[output_index] |= 1 << (y & 7);
   }

   print!("const BUFFER: [u8; {}] = [", BITMAP_BUFFER_SIZE / 8);
   for index in 0..(BITMAP_BUFFER_SIZE / 8)  {
      if (index % 16) == 0 { println!(); }
      print!("0x{:02X}, ", output_buffer[index]);
   }
   println!("\n];");
}

